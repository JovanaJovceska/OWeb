function addItem() {
    var ul = document.getElementById("dynamic-listUName");
    var ul1 = document.getElementById("dynamic-listCom");
    var name = document.getElementById("uname");
    var comment = document.getElementById("wText");
    
    var li = document.createElement("li");
    li.setAttribute('id', name.value);
    li.appendChild(document.createTextNode(name.value));
    ul.appendChild(li);

    var li1 = document.createElement("li");
    li1.setAttribute('id', comment.value);
    li1.appendChild(document.createTextNode(comment.value));
    ul1.appendChild(li1);
}

